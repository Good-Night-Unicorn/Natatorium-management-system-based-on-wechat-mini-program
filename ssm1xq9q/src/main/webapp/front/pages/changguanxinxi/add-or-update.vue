			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`changguanxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='changguanbianhao'){
					this.ruleForm.changguanbianhao = obj[o];
					this.ro.changguanbianhao = true;
					continue;
					}
					if(o=='changguanmingcheng'){
					this.ruleForm.changguanmingcheng = obj[o];
					this.ro.changguanmingcheng = true;
