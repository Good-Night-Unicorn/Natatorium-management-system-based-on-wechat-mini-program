							await this.$api.add(`changguanxinxi`, this.ruleForm);
						}
						this.$utils.msgBack('提交成功');
					}
				} else {
                //跨表计算
					if(this.ruleForm.id){
						await this.$api.update(`changguanxinxi`, this.ruleForm);
					}else{
						await this.$api.add(`changguanxinxi`, this.ruleForm);
					}
					this.$utils.msgBack('提交成功');
				}
